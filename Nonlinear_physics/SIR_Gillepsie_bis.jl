begin
	# --> Transition rates.
	infection_rate(u, p, t) = p[1] * u[1] * u[2]
	recovery_rate(u, p, t) = p[2] * u[2]
	hospitalization_rate(u, p, t) = p[3] * u[2]
	discharge_rate(u, p, t) = p[4] * u[3]

	# --> Transitions.
	function infection!(integrator)
		integrator.u[1] -= 1
		integrator.u[2] += 1
		return
	end

	function recovery!(integrator)
		integrator.u[2] -= 1
		integrator.u[4] += 1
		return
	end

	function hospitalization!(integrator)
		integrator.u[2] -= 1
		integrator.u[3] += 1
		return
	end

	function discharge!(integrator)
		integrator.u[3] -= 1
		integrator.u[4] += 1
		return
	end

	# --> DiffEq interface.
	infection_jump = ConstantRateJump(infection_rate, infection!)
	recovery_jump = ConstantRateJump(recovery_rate, recovery!)
	hospitalization_jump = ConstantRateJump(hospitalization_rate, hospitalization!)
	discharge_jump = ConstantRateJump(discharge_rate, discharge!)
end

# ╔═╡ c986317a-122b-44c0-88ae-3610aebaf5d3
function simulate(params, ϵ=0.001)

	# --> Parameters.
	N, R₀, vax, α, τi, τh, tmax = params

	# --> Convert timescale from weeks to day.
	τi = 7 * τi
	τh = 7 * τh

	# --> Simulation time.
	tmax = tmax * 365

	# --> Parameters for the simulation.
	γ = 1/τi
	β = R₀ * γ / N
	α = α / 100
	ν = min(1 / τh, 1000)

	p = β, γ, α, ν
	tspan = (0.0, tmax)

	# --> Initial condition.
	I₀ = ceil(Int, N*ϵ)
	V₀ = ceil(Int, N*vax/100)
	u₀ = [N-I₀-V₀, I₀, 0, V₀]

	# --> Discrete-time problem.
	prob = DiscreteProblem(u₀, tspan, p)

	# --> Jump problem.
	jump_prob = JumpProblem(
		prob,
		Direct(),
		infection_jump,
		recovery_jump,
		hospitalization_jump,
		discharge_jump,
		save_positions=(false, false)
	)

	# --> Solve.
	sol = solve(jump_prob, SSAStepper(), saveat=1)
	
	return sol
end

# ╔═╡ c27f9c8a-4b02-42b0-9040-5d18b70f6ee1
function plot_epidemie(epidemie, params)

	p1 = plot(size=(600, 200))
	# --> Rescale time to weeks.
	t = epidemie.t / 7
	scale = params[1] / 70e6

	# --> Susceptible population.
	p1 = plot!(t, epidemie[1, :]/scale, color="dodgerblue", lw=2, label="S")
	p1 = plot!(t, (epidemie[2, :] + epidemie[3, :])/scale, color="red", lw=2, label="I+H")
	p1 = plot!(t, epidemie[4, :]/scale, color="gray", lw=2, label="R")

	p2 = plot(size=(600, 200))
	p2 = plot!(t, epidemie[3, :]/scale, color="orange", lw=2, label="H")

	p = plot(p1, p2, layout=(2, 1), size=(600, 400), link=:x)

	xlabel!("Semaines depuis le début de l'infection")
	ylabel!("Population")
	return p
end

